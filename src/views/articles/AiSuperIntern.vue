                <li><strong>SOP 制定</strong><br />"请帮我写一份新媒体运营新人入职 SOP，按第一周每天拆任务。"</li>
                <li><strong>模拟谈话</strong><br />"你扮演一个绩效不达标但不服气的员工，我来和你谈，请指出我的问题。"</li>
