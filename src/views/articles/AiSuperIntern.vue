            <p>搜索 → 阅读 → 整理 → 思考 → 产出<br />（中途很容易卡住或放弃）</p>
          </div>
          <div class="comparison-card new">
            <h4>新习惯（AI 逻辑）</h4>
            <p><strong>提问（Prompt） → AI 给初稿 → 人类修改 → 完稿</strong></p>
          </div>
        </div>

        <p>我自己有一句非常土、但极其好用的口诀：</p>

        <div class="principle-highlight">
          <strong>遇事不决问AI。</strong>
        </div>

        <h3>🔁 一个很重要的反直觉点</h3>

        <p>很多新手心里会有个坎：</p>

        <blockquote>
          "我一上来就问 AI，是不是代表我不思考了？"
        </blockquote>

        <p>恰恰相反。</p>

        <p><strong>真正的变化不是「我不想了」，</strong><br /><strong>而是我把思考顺序换了。</strong></p>

        <div class="comparison-grid">
          <div class="comparison-card old">
