                <li><strong>Excel / 公式 / 代码</strong><br />"我想把 A 列日期转成 B 列格式，公式怎么写？"</li>
                <li><strong>职场沟通</strong><br />"帮我把这段话改得更专业一点，发给客户李总。"</li>
              </ul>
            </div>
          </div>

          <div class="role-card mid">
            <div class="role-header">
              <span class="role-icon">🧑‍💼</span>
              <span class="role-title">中层干部（管理层）：它是你的「流程参谋」</span>
            </div>
            <div class="role-body">
