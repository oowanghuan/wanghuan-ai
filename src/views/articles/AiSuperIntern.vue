        <h2>1️⃣ 祛魅：AI 到底是什么？</h2>

        <p>先把神话打碎。</p>

        <p>目前我们用到的这些 AI（ChatGPT、Kimi、DeepSeek、Claude 之类），<br /><strong>本质上并不是一个"有意识的大脑"</strong>，而是：</p>

        <div class="definition-box">
          <p>一个基于概率的语言模型<br />——说人话，就是「文字接龙高手 + 超级资料库」。</p>
          <p class="note">也就是AI生成的每一个字都是基于概率猜出来的，这是Transformer这个底层核心架构决定的（看不懂这句话不重要，忽略就行）</p>
        </div>

        <p>它最擅长的，其实只有两件事：</p>

        <div class="two-things">
          <div class="thing-card">
            <h4>第一，生成。</h4>
            <p>从 0 到 1 帮你写东西：邮件、文案、方案、代码、总结。</p>
          </div>
          <div class="thing-card">
            <h4>第二，总结。</h4>
            <p>把你没时间看的长内容，压缩成你几分钟能看懂的版本。</p>
          </div>
        </div>

        <p>所以你可以把它理解成：</p>

        <div class="intern-box">
          <p>一个读过全人类互联网内容的「超级实习生」<br />反应快、不抱怨、24 小时待命<br />但偶尔会一本正经地胡说八道。</p>
        </div>

        <p>它需要你给清楚指令（Prompt），<br />也需要你来把关审核（Review）。</p>

        <hr />

        <h2>2️⃣ 别光看，现在就拿出手机试一下</h2>

        <p>说再多都没用。<br /><strong>AI 这种东西，你必须亲手用一次，才会真正"通电"。</strong></p>

        <p>现在，请你真的停下来 1 分钟，拿出手机。</p>

        <div class="step-guide">
          <div class="step-card">
            <div class="step-number">第一步</div>
            <div class="step-content">
