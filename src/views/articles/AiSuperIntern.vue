        <p>相比以上这些给你现成的工具，我其实更想给你下面这段话：</p>

        <h3>🧠「AI 工具推荐通用 Prompt」</h3>

        <div class="prompt-box large">
          <div class="prompt-content">
            <p>请你作为一名 <strong>AI 工具顾问</strong>，帮助我判断我是否真的需要额外工具，以及如果需要，应该用哪一类工具。</p>
            <p>我的背景与需求如下：</p>
            <p><strong>1️⃣ 我想完成的事情是：</strong><br />（一句话描述目标）</p>
            <p><strong>2️⃣ 这个任务的输出形式是：</strong><br />（例如：文章 / PPT / 视频 / 代码 / 调研结论 / 方案）</p>
            <p><strong>3️⃣ 我的熟练程度是：</strong><br />（新手 / 有一点经验 / 非专业但常用 / 专业人士）</p>
            <p><strong>4️⃣ 我的时间与精力限制是：</strong><br />（例如：想快点完成 / 不想学复杂工具 / 可以花时间折腾）</p>
            <p><strong>5️⃣ 我的核心痛点是：</strong><br />（不知道从哪开始 / 信息太多 / 不会判断好坏 / 不会表达需求）</p>
            <p>请你：</p>
            <ul>
              <li>先判断：<strong>只用一个聊天机器人是否已经足够？</strong></li>
              <li>如果不够，请推荐 <strong>最少数量的工具组合</strong></li>
              <li>并说明：
                <ul>
                  <li>为什么需要它</li>
                  <li>用到什么程度就可以停</li>
                  <li>不需要做哪些"多余的事"</li>
                </ul>
              </li>
            </ul>
            <p>请避免推荐过多工具，<br />目标是 <strong>最低学习成本 + 可执行</strong>。</p>
