        <h2>3️⃣ 最重要的不是工具，而是思维习惯的转变</h2>

        <p>很多人觉得 AI 不好用，问题不在 AI，<br />而在于<strong>还在用搜索引擎时代的习惯</strong>。</p>

        <div class="comparison-grid">
          <div class="comparison-card old">
