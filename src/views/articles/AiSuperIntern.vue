        <h2>4️⃣ 职场分层实战：你在哪一层，就在哪一层用它</h2>

        <p>无论你在职场的哪个阶段，<br />AI 都能成为你的能力放大器。</p>

        <div class="role-section">
          <div class="role-card junior">
            <div class="role-header">
              <span class="role-icon">👶</span>
              <span class="role-title">初入职场（执行层）：它是你的「全能外挂」</span>
