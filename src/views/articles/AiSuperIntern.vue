        </div>

        <div class="prompt-box">
          <div class="prompt-header">请复制这段指令（Prompt）：</div>
          <div class="prompt-content">
            <p>"你现在是一位高情商的职场沟通专家。<br />我刚刚收到一个项目需求，但因为目前手头工作已经排满了，且这个需求不属于我的核心 KPI，我需要委婉地拒绝同事。</p>
            <p>请帮我写一段回复，要求：</p>
            <ul>
              <li>语气礼貌但态度坚定；</li>
              <li>不要直接说'不'，而是提出具体的排期困难；</li>
              <li>给出一个替代方案（比如下周再聊，或者推荐其他人）。</li>
            </ul>
            <p>请给我生成 <strong>2 个不同版本</strong> 的回复供我选择。"</p>
          </div>
        </div>

        <div class="step-guide">
          <div class="step-card">
            <div class="step-number">第三步</div>
            <div class="step-content">
              <p>回车。<br /><strong>停 10 秒。</strong></p>
              <p>认真看看它生成的答案。</p>
            </div>
          </div>
        </div>

        <p>现在我想问你一句很现实的话：</p>

        <blockquote>
          是不是比你自己在那里纠结半小时，<br />写出来的还要得体、冷静、像个"职场老手"？
        </blockquote>

        <p>如果你点头了，那恭喜你——<br />你已经完成了 <strong>AI 使用的第一课</strong>。</p>

        <p>这就叫：</p>

        <div class="principle-highlight">
          <strong>遇事不决问AI。</strong>
