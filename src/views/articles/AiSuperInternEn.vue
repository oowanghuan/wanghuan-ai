            <div class="aspect-content">Is my Context sufficient?</div>
          </div>
          <div class="aspect-card">
            <div class="aspect-number">3</div>
            <div class="aspect-content">Is my Task broken down enough?</div>
          </div>
        </div>

        <p>These three points (Prompt accuracy, Context sufficiency, Task granularity) aren't just techniques — they're <strong>"engineering thinking"</strong> applied practically. This is the divide between casual users and power users. Casual users see AI bullshitting and think "this thing sucks." Power users think "maybe I didn't ask well."</p>

        <p>Let me give you concrete examples:</p>

        <p>If AI gives you garbage, don't close the window yet. In my experience, 90% of failures come from us. Whenever I think AI is "dumb," I immediately check these three things:</p>

        <div class="check-section">
          <div class="check-card">
            <h4>Question 1: Is the Prompt written correctly?</h4>
            <p>Are you using the same "vague language" you'd use with people?</p>
            <div class="example wrong">
              <span class="label">Wrong</span>
              <p>"Help me write some copy." (Too vague, AI can only guess)<br />(Imagine if your boss said this to you — what would you do?)</p>
            </div>
            <div class="example right">
              <span class="label">Right</span>
              <p>"You are an experienced social media blogger. Help me write a product recommendation post for a whitening serum. Tone should be lively, targeting working women who stay up late, highlighting 'removes dullness' as the selling point."</p>
            </div>
            <p class="tip"><strong>Tip:</strong> Give a role, give a goal, give a tone.</p>
          </div>

          <div class="check-card">
            <h4>Question 2: Is the Context sufficient?</h4>
            <p>AI can't read minds. It doesn't know your "assumed context."</p>
            <div class="example wrong">
              <span class="label">Wrong</span>
              <p>"Optimize this code/text."<br />(Imagine receiving this task as a human — how would you optimize it?)</p>
            </div>
            <div class="example right">
              <span class="label">Right</span>
              <p>"Here's my project background (attached doc), I'm developing with Vue.js, and I got this error (attached error log). Based on this information, help me optimize this code."</p>
            </div>
            <p class="tip"><strong>Tip:</strong> Feed it as much background information from your brain as possible. <strong>Often AI seems dumb because it's in an "information vacuum."</strong></p>
          </div>

          <div class="check-card">
            <h4>Question 3: Is the Task broken down enough?</h4>
            <p>You can't eat an elephant in one bite, and neither can AI. If you throw it a super complex task, it will likely miss things here and there due to "scattered attention."</p>
            <div class="example wrong">
              <span class="label">Wrong</span>
              <p>"Help me make an app like TikTok." (This will crash AI)<br />(You'd just call your boss crazy if you got this order. AI will just produce garbage.)</p>
            </div>
            <div class="example right">
              <span class="label">Right</span>
              <p>"Step 1, help me design TikTok's user registration flowchart; Step 2, based on the flowchart, generate database table structure; Step 3, help me write the login page frontend code..."</p>
            </div>
            <p class="tip"><strong>Tip:</strong> <strong>Break it down.</strong> Guide it step by step, and you'll find its IQ suddenly comes online.</p>
          </div>
        </div>

