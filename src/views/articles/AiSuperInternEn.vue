        <h2>One More Thing...</h2>

        <p>AI isn't learned — it's used. No "AI course" or "AI tool guide" can universally solve your AI usage. AI is a tool — just use it. Once you use it, you'll know what problems exist and how to fix them. Learn by doing.</p>

        <p>Of course, you'll encounter AI making mistakes. Here's how I see it:</p>

        <h3>Believe in AI, But Don't Trust It Blindly</h3>

        <p><strong>I believe in its capability</strong> (it has read ten thousand times more than me), <strong>but I never blindly trust its output</strong> (it occasionally bullshits with complete confidence).</p>

        <p>Once you accept this, your relationship changes: <strong>It's not your "teacher" — it's your "super intern" or "co-pilot."</strong> Since you're managing an intern, when they do poorly, it's often because we didn't guide them well.</p>

        <p>AI's capability is definitely there. Its brain has far more than mine. My job is to extract what I want. If the output isn't what I want, I think about three things:</p>

